// Given a string that consists of left and right parentheses, '(' and ')',
// balance the parentheses by inserting parentheses as necessary.
// Determine the minimum number characters that must be inserted.

function getMin(s) {
  let leftOne = 0;
  let rightOne = 0;

  Array.from(s).filter((el) => {
    if (el == "(") {
      leftOne++;
    } else {
      rightOne++;
    }
  });

  console.log(Math.abs(leftOne - rightOne));

  return Math.abs(leftOne - rightOne);
}

